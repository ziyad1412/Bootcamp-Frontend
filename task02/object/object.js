/**
 * Membuat object menggunakan {}.
 * Menyimpan nilai dengan format key : value
 */
const user = {
  name: "Aufa Billah",
  age: 22,
  major: "Informatics",
};

/**
 * Mengakses nilai object menggunakan key.
 * Cara akses bisa menggunakan dot atau bracket
 */
console.log(user.name, user["age"]);
